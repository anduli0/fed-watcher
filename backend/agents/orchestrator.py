# All agents — 9 specialists + 12 regional
SPECIALIST_AGENTS: list[BaseAgent] = [a1, a2, a3, a4, a5, a6, a7, a8, a10]
ALL_AGENTS: list[BaseAgent] = SPECIALIST_AGENTS + REGIONAL_AGENTS
# Pre-built O(1) lookup (populated after _AGENT_BY_ID function definition below)
_AGENT_BY_ID: dict[int, BaseAgent] = {}

# Collaboration parameters
OUTLIER_BPS_THRESHOLD = 28.0   # |delta - mean| > 28 bps → trigger Round 2 (was 30)
MAX_OUTLIERS_REVIEWED = 5       # review more outliers (was 3) — better consensus formation

# Confidence-based filtering / weighting
MIN_CONFIDENCE_FOR_INCLUSION = 0.30   # raised from 0.25 — keep only meaningful signals
CONFIDENCE_EXPONENT = 2.5              # raised from 2.0 — reward high-conviction more aggressively

# Committee disagreement penalty (Bayesian posterior tightness)
TIGHT_CONSENSUS_STD   = 22.0   # bps stdev — below this, agents agree → confidence boost
WIDE_DISAGREEMENT_STD = 65.0   # bps stdev — above this, scattered → confidence penalty
CONSENSUS_BOOST       = 1.18   # tight consensus adds 18% to aggregate confidence
DISAGREEMENT_PENALTY  = 0.78   # wide spread cuts aggregate confidence by 22%
MAX_AGGREGATE_CONF    = 0.93   # cap aggregate confidence (never claim certainty)

_WEIGHT_OVERRIDES: dict[int, float] = {}
_ADAPTIVE_MULTIPLIERS: dict[int, float] = {}  # set by adaptive_weights()


def apply_weight_override(agent_id: int, weight: float):
    _WEIGHT_OVERRIDES[agent_id] = weight


async def compute_adaptive_weights(db, event: dict | None = None) -> dict[int, float]:
    """
    Auto-tune weights based on:
    1. Historical accuracy (from feedback_entry divergences)
    2. Current event type (FOMC/CPI/NFP boosts relevant agents)
    3. Capped at [0.5×, 2.0×] multiplier
    """
    from sqlalchemy import select, func
    from backend.database.models import FeedbackEntry
    multipliers: dict[int, float] = {}

    # ── Accuracy-based tuning ──────────────────────────────────────────────
    try:
        result = await db.execute(
            select(FeedbackEntry.agent_id,
                   func.avg(FeedbackEntry.divergence_bps).label("avg_div"),
                   func.count(FeedbackEntry.id).label("n"))
            .group_by(FeedbackEntry.agent_id)
            .having(func.count(FeedbackEntry.id) >= 3)   # min 3 samples
        )
        rows = result.all()
        if rows:
            divs = [row.avg_div for row in rows]
            median_div = sorted(divs)[len(divs) // 2]
            for row in rows:
                if row.avg_div < median_div * 0.6:
                    multipliers[row.agent_id] = 1.35   # accurate → boost
                elif row.avg_div > median_div * 1.6:
                    multipliers[row.agent_id] = 0.65   # inaccurate → penalise
            if multipliers:
                AL.system_event(f"Adaptive weights: {len(multipliers)} agents adjusted from accuracy history")
    except Exception:
        pass

    # Enforce [0.5×, 2.0×] cap on all multipliers
    multipliers = {k: max(0.5, min(2.0, v)) for k, v in multipliers.items()}

    # ── Event-based boosts ─────────────────────────────────────────────────
    if event:
        label = event.get("label", "")
        if "FOMC" in label:
            for aid in [1, 5, 6]:   # Behavioral, Projections, FOMC_Minutes
                multipliers[aid] = max(multipliers.get(aid, 1.0), 1.4)
            AL.system_event(f"FOMC event: Behavioral/Projections/Minutes weights boosted 1.4×")
        elif "CPI" in label:
            for aid in [4, 7]:      # Academic (Taylor), Macro_Data
                multipliers[aid] = max(multipliers.get(aid, 1.0), 1.4)
            AL.system_event(f"CPI event: Academic/MacroData weights boosted 1.4×")
        elif "NFP" in label:
            for aid in [7]:         # Macro_Data
                multipliers[aid] = max(multipliers.get(aid, 1.0), 1.4)
            for aid in range(101, 113):  # All regional Feds (labor market)
                multipliers[aid] = max(multipliers.get(aid, 1.0), 1.25)
            AL.system_event(f"NFP event: MacroData + Regional Feds weights boosted")

    global _ADAPTIVE_MULTIPLIERS
    _ADAPTIVE_MULTIPLIERS = multipliers
    return multipliers


def _effective_weight(result: AgentResult, base_weight: float) -> float:
    w = _WEIGHT_OVERRIDES.get(result.agent_id, base_weight)
    # Apply adaptive multiplier (accuracy + event-based)
    w *= _ADAPTIVE_MULTIPLIERS.get(result.agent_id, 1.0)
    if result.limited_mode:
        w *= 0.3
    if result.agent_id == 10:  # Consensus premium
        w *= 1.5
    return max(0.1, w)  # floor at 0.1 — never zero


