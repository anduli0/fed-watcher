def _aggregate_horizon(valid: list[AgentResult], horizon: str) -> tuple[float, float]:
    """
    Bayesian-style precision-weighted aggregation with committee dynamics.

    Pipeline:
    1. Prune agents below MIN_CONFIDENCE_FOR_INCLUSION
    2. Compute precision-weighted mean (weight ∝ effective_weight × confidence^EXPONENT)
    3. Apply committee-disagreement adjustment:
       - Tight consensus (std < TIGHT_CONSENSUS_STD): +18% confidence boost
       - Wide disagreement (std > WIDE_DISAGREEMENT_STD): -22% confidence penalty
    4. Cap at MAX_AGGREGATE_CONF (never report certainty)
    """
    import statistics
    total_weight = 0.0
    weighted_delta = 0.0
    weighted_confidence = 0.0
    included_deltas: list[float] = []
    included_weights: list[float] = []

    for r in valid:
        h_conf = r.horizon_confidence(horizon)
        if h_conf < MIN_CONFIDENCE_FOR_INCLUSION:
            continue
        agent_obj = _AGENT_BY_ID.get(r.agent_id)
        base_weight = agent_obj.weight if agent_obj else 1.0
        w = _effective_weight(r, base_weight) * (h_conf ** CONFIDENCE_EXPONENT)
        delta = r.horizon_delta(horizon)
        weighted_delta += delta * w
        weighted_confidence += h_conf * w
        total_weight += w
        included_deltas.append(delta)
        included_weights.append(w)

    if total_weight == 0:
        return 0.0, 0.0

    weighted_delta /= total_weight
    weighted_confidence /= total_weight

    # Committee disagreement adjustment — quantify posterior tightness
    if len(included_deltas) >= 4:
        # Weighted standard deviation around the consensus
        var = sum(
            wi * (di - weighted_delta) ** 2
            for di, wi in zip(included_deltas, included_weights)
        ) / total_weight
        std = var ** 0.5

        if std < TIGHT_CONSENSUS_STD:
            weighted_confidence *= CONSENSUS_BOOST
        elif std > WIDE_DISAGREEMENT_STD:
            weighted_confidence *= DISAGREEMENT_PENALTY
        # Else: middle band — no adjustment

    # Cap at max aggregate confidence
    weighted_confidence = min(MAX_AGGREGATE_CONF, weighted_confidence)
    return weighted_delta, weighted_confidence


def _identify_outliers(valid: list[AgentResult], consensus_12m: float) -> list[AgentResult]:
    """Find agents whose 12m signal diverges materially from consensus."""
    outliers = []
    for r in valid:
        divergence = abs(r.horizon_delta("12m") - consensus_12m)
        if divergence > OUTLIER_BPS_THRESHOLD:
            outliers.append((divergence, r))
    outliers.sort(key=lambda t: -t[0])
    return [r for _, r in outliers[:MAX_OUTLIERS_REVIEWED]]


async def run_full_cycle(ctx: AgentContext, cycle_type: str = "scheduled") -> dict:
    """
    Two-round orchestration:
      Round 1: All 21 agents analyze independently
      Round 2: Outliers see consensus & may revise (collaboration)
    Then aggregate all 4 horizons + generate derivation report.
    """
    # ── ROUND 1: Independent analysis ──────────────────────────────────────
    AL.orchestrator_event(f"Cycle start: {len(ALL_AGENTS)} agents dispatched in parallel (Round 1)")

    tasks = [agent.run(ctx) for agent in ALL_AGENTS]
    raw_results = await asyncio.gather(*tasks, return_exceptions=True)

    valid: list[AgentResult] = []
    errors: list[dict] = []
    for agent, r in zip(ALL_AGENTS, raw_results):
        if isinstance(r, Exception):
            errors.append({"agent": agent.agent_name, "error": str(r)[:200]})
            AL.collect_failed(agent.agent_name, str(r)[:60])
        else:
            valid.append(r)
            AL.agent_done(r.agent_name, r.signal, r.rate_path_delta_bps, r.confidence)

    # First-pass 12m consensus
    consensus_12m, _ = _aggregate_horizon(valid, "12m")

    # ── ROUND 2: Collaboration on outliers ─────────────────────────────────
    outliers = _identify_outliers(valid, consensus_12m)
    revisions: dict[int, AgentResult] = {}

    if outliers:
        import statistics as _stat
        AL.orchestrator_event(f"Round 2: {len(outliers)} outlier agents reviewing consensus — {', '.join(o.agent_name for o in outliers)}")

        # Signal distribution
        signal_dist = {"hawkish": 0, "neutral": 0, "dovish": 0}
        for r in valid:
            signal_dist[r.signal] = signal_dist.get(r.signal, 0) + 1

        # Find the 3 agents CLOSEST to consensus (best counter-evidence vs random sample)
        agents_by_distance = sorted(
            valid,
            key=lambda r: abs(r.horizon_delta("12m") - consensus_12m)
        )
        anchors = agents_by_distance[:3]

        # Median + range for context
        all_12m = [r.horizon_delta("12m") for r in valid]
        median_12m = _stat.median(all_12m)
        lo, hi = min(all_12m), max(all_12m)

        async def review_outlier(orig: AgentResult) -> AgentResult:
            agent_obj = _AGENT_BY_ID.get(orig.agent_id)
            if agent_obj is None:
                return orig

            # Quantify exactly how far this outlier sits from the committee
            outlier_error_bps = orig.horizon_delta("12m") - consensus_12m
            error_direction = "MORE HAWKISH" if outlier_error_bps > 0 else "MORE DOVISH"

            # Build targeted feedback (not generic)
            anchor_reasoning = " | ".join(
                f"[{a.agent_name} {a.horizon_delta('12m'):+.0f}bps]: {a.reasoning[:120]}"
                for a in anchors
            )
            consensus_summary = (
                f"COMMITTEE CONSENSUS (12m): {consensus_12m:+.1f} bps "
                f"[median {median_12m:+.0f}, range {lo:+.0f}..{hi:+.0f}]. "
                f"Distribution: hawkish={signal_dist['hawkish']}, "
                f"neutral={signal_dist['neutral']}, dovish={signal_dist['dovish']} "
                f"(of {len(valid)}).\n"
                f"YOUR R1 OUTPUT IS {abs(outlier_error_bps):.0f} bps {error_direction} "
                f"than consensus. Reconsider the following:\n"
                f"  1. Are you over-weighting one data source vs peers?\n"
                f"  2. Are you assuming a regime shift others don't see?\n"
                f"  3. Could you be anchoring on a recent event peers have already discounted?\n\n"
                f"3 AGENTS CLOSEST TO CONSENSUS: {anchor_reasoning}\n\n"
                f"Either (a) provide stronger evidence to defend your contrarian view, "
                f"or (b) revise toward consensus. Epistemic humility is rewarded — "
                f"updating in light of new information is NOT weakness."
            )

            review_ctx = AgentContext(
                macro_snapshot_text=ctx.macro_snapshot_text,
                speeches_text=ctx.speeches_text,
                fomc_minutes_texts=ctx.fomc_minutes_texts,
                beige_book_text=ctx.beige_book_text,
                regional_stances_text=ctx.regional_stances_text,
                cme_probabilities=ctx.cme_probabilities,
                negative_examples=ctx.negative_examples,
                material_event=ctx.material_event,
                consensus_summary=consensus_summary,
                own_round1_output=(
                    f"signal={orig.signal}, 12m_delta={orig.rate_path_delta_bps:+.0f} bps, "
                    f"confidence={orig.confidence:.2f}, reasoning={orig.reasoning[:200]}"
                ),
            )
            try:
                revised = await agent_obj.run(review_ctx)
                revised.round = 2
                revised.revised = abs(revised.rate_path_delta_bps - orig.rate_path_delta_bps) > 5
                return revised
            except Exception:
                return orig

        review_results = await asyncio.gather(*[review_outlier(o) for o in outliers])
        for rev in review_results:
            revisions[rev.agent_id] = rev
            AL.agent_done(rev.agent_name, rev.signal, rev.rate_path_delta_bps, rev.confidence, revised=rev.revised)

    # Replace round-1 outputs with round-2 revisions
    final_results = [revisions.get(r.agent_id, r) for r in valid]

    # ── Multi-horizon aggregation ─────────────────────────────────────────
    horizon_aggregates: dict[str, dict] = {}
    for h in HORIZONS:
        delta, conf = _aggregate_horizon(final_results, h)
        # Determine signal at this horizon
        if delta >= 25:
            signal = "hawkish"
        elif delta <= -25:
            signal = "dovish"
        else:
            signal = "neutral"
        horizon_aggregates[h] = {
            "weighted_delta_bps": round(delta, 2),
            "confidence": round(conf, 3),
            "signal": signal,
        }

    # ── Summary log ───────────────────────────────────────────────────────
    for h in HORIZONS:
        agg = horizon_aggregates[h]
        AL.orchestrator_event(
            f"{h.upper()} forecast → {agg['signal'].upper()} {agg['weighted_delta_bps']:+.0f} bps "
            f"(conf {agg['confidence']:.0%})"
        )

    # ── Chief synthesis + derivation report (EN + KO in parallel) ────────
    AL.orchestrator_event("Chief synthesizing derivation report (EN + KO)…")
    report_en, report_ko = await asyncio.gather(
        _generate_derivation_report(final_results, horizon_aggregates, outliers, revisions, errors, ctx, lang="en"),
        _generate_derivation_report(final_results, horizon_aggregates, outliers, revisions, errors, ctx, lang="ko"),
    )

    return {
        "cycle_type": cycle_type,
        "timestamp": datetime.utcnow().isoformat(),
        "horizons": horizon_aggregates,
        "report_en": report_en,
        "report_ko": report_ko,
        "agent_results": [
            {
                "agent_id": r.agent_id,
                "agent_name": r.agent_name,
                "signal": r.signal,
                "rate_path_delta_bps": r.rate_path_delta_bps,
                "confidence": r.confidence,
                "horizons": {
                    h: {
                        "delta_bps": r.horizon_delta(h),
                        "confidence": r.horizon_confidence(h),
                        "rationale": (r.horizons[h].rationale if h in r.horizons else ""),
                    }
                    for h in HORIZONS
                },
                "weight_applied": _effective_weight(
                    r, (_AGENT_BY_ID[r.agent_id].weight if r.agent_id in _AGENT_BY_ID else 1.0)
                ),
                "limited_mode": r.limited_mode,
                "duration_ms": r.duration_ms,
                "round": r.round,
                "revised": r.revised,
                "reasoning": r.reasoning,
            }
            for r in final_results
        ],
        "errors": errors,
        "collaboration": {
            "outliers_reviewed": [r.agent_name for r in outliers],
            "agents_revised": [r.agent_name for r in revisions.values() if r.revised],
        },
        "report_text": report_ko,   # backward compat field keeps Korean
    }


async def _generate_derivation_report(
    results: list[AgentResult],
    horizon_aggregates: dict[str, dict],
    outliers: list[AgentResult],
    revisions: dict[int, AgentResult],
    errors: list[dict],
    ctx: AgentContext,
    lang: str = "en",
) -> str:
    """Chief generates a markdown derivation report in the requested language."""
    agent_rows = []
    for r in sorted(results, key=lambda x: x.agent_id):
        row = (
            f"| {r.agent_name} | {r.signal.upper()} | "
            f"{r.horizon_delta('6m'):+.0f} | {r.horizon_delta('12m'):+.0f} | "
            f"{r.horizon_delta('3y'):+.0f} | {r.horizon_delta('10y'):+.0f} | "
            f"{r.confidence:.2f} |"
        )
        if r.revised:
            row += " *(revised)*"
        agent_rows.append(row)
    agent_table = "\n".join(agent_rows)

    horizon_rows = "\n".join(
        f"| {h.upper()} | {horizon_aggregates[h]['weighted_delta_bps']:+.1f} bps | "
        f"{horizon_aggregates[h]['signal'].upper()} | {horizon_aggregates[h]['confidence']:.2f} |"
        for h in HORIZONS
    )

    outlier_text = ", ".join(o.agent_name for o in outliers) if outliers else "None — broad consensus"
    revised_text = ", ".join(r.agent_name for r in revisions.values() if r.revised) or "None"

    if lang == "ko":
        report_instruction = (
            "Write a derivation report in KOREAN (한국어) — under 700 characters — covering:\n"
            "1. **요약**: 4개 horizon 한 줄씩 요약\n"
            "2. **주요 데이터 동인**: 어떤 macro/연설/이벤트가 신호를 이끌었는지 (2-3개)\n"
            "3. **주요 이견**: 어느 에이전트들이 다른 방향이었는지, 왜\n"
            "4. **핵심 리스크**: 이 예측을 무효화할 수 있는 1-2개 시나리오"
        )
    else:
        report_instruction = (
            "Write a derivation report in ENGLISH — under 700 characters — covering:\n"
            "1. **Summary**: One line per horizon\n"
            "2. **Key data drivers**: Which macro/speech/event led the signal (2-3 items)\n"
            "3. **Key divergences**: Which agents differed and why\n"
            "4. **Key risks**: 1-2 scenarios that would invalidate this forecast"
        )

    prompt = f"""You are the Chief Macroeconomic Orchestrator. Generate a concise markdown derivation report.

## Aggregated Horizon Outputs
| Horizon | Delta | Signal | Confidence |
|---------|-------|--------|-----------|
{horizon_rows}

## Agent Outputs (delta in bps)
| Agent | Signal | 6M | 12M | 3Y | 10Y | Conf |
|-------|--------|-----|-----|----|----|------|
{agent_table}

## Collaboration
- Outliers reviewed: {outlier_text}
- Agents that revised: {revised_text}
- Failed agents: {len(errors)}

## Macro Snapshot
{ctx.macro_snapshot_text[:1200]}

## Material Event
{ctx.material_event or "None"}

{report_instruction}

Output ONLY the markdown report, no preamble.
"""
    client = anthropic.AsyncAnthropic(api_key=settings.ANTHROPIC_API_KEY)
    try:
        resp = await client.messages.create(
            model=settings.MODEL_ID,
            max_tokens=1500,
            messages=[{"role": "user", "content": prompt}],
        )
        return resp.content[0].text
    except Exception as e:
        return f"Report generation failed: {e}"
